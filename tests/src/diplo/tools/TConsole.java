package diplo.tools;

public class TConsole {
    /**
     * Show a string
     * @param toPrint
     */
    public static void toprint(String toPrint) {
        System.out.print(toPrint);
    }
    /**
     * Show an integer
     * @param intPrint
     */
    public static void toprint(int intPrint) {
        System.out.print(String.valueOf(intPrint));
    }
    /**
     * Show a float
     * @param floatPrint
     */
    public static void toprint(float floatPrint) {
        System.out.print(String.valueOf(floatPrint));
    }
    /**
     * Show a boolean
     * @param booleanPrint
     */
    public static void toprint(boolean booleanPrint) {
        System.out.print(String.valueOf(booleanPrint));
    }
/*****************************************************************************************/
/**************************** WITH BREAK *************************************************/
/*****************************************************************************************/
    /**
     * Show a string with break
     * @param toPrint
     */
    public static void toprintln(String toPrint) {
        System.out.println(toPrint);
    }
    /**
     * Show an integer with break
     */
    public static void toprintln(int intPrint) {
        System.out.println(String.valueOf(intPrint));
    }
    /**
     * Show a float with break
     */
    public static void toprintln(float floatPrint) {
        System.out.println(String.valueOf(floatPrint));
    }
    /**
     * Show a boolean with break
     */
    public static void toprintln(boolean booleanPrint) {
        System.out.println(String.valueOf(booleanPrint));
    }
}
